# The division rule says:

# 𝑎=𝑏×𝑞+𝑟
# a=b×q+r

# where

# a = dividend
# b = divisor
# q = quotient (how many full times it fits)
# r = remainder (what’s left)
# and

# 0≤𝑟<∣𝑏∣
# 0≤r<∣b∣

print(4/2)
print(1.5/2)
print(-2/2)

